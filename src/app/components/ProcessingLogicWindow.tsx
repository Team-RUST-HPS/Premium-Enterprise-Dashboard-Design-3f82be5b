// ─── Syntax-highlighted EDI line ─────────────────────────────────────────────
function EDILine({ line, lineNum }: { line: string; lineNum: number }) {
  if (!line.trim()) return (
    <div className="flex">
      <span style={{ width: 38, color: "rgba(255,255,255,0.15)", fontSize: "0.65rem", userSelect: "none", flexShrink: 0, textAlign: "right", paddingRight: 14 }}>{lineNum}</span>
    </div>
  );

  const parts = line.replace(/~$/, "").split("*");
  const seg   = parts[0];
  const elems = parts.slice(1);

  const segColor: Record<string, string> = {
    ISA: "#3B82F6", GS: "#3B82F6", ST: "#3B82F6", SE: "#3B82F6", GE: "#3B82F6", IEA: "#3B82F6",
    BGN: "#F59E0B", REF: "#F59E0B", DTP: "#F59E0B",
    N1: "#8B5CF6", N3: "#8B5CF6", N4: "#8B5CF6",
    INS: "#10B981", NM1: "#10B981", PER: "#10B981", DMG: "#10B981", HD: "#10B981",
  };
  const color = segColor[seg] ?? "#94A3B8";

  return (
    <div className="flex hover:bg-white/[0.03] rounded transition-colors">
      <span style={{ width: 38, color: "rgba(255,255,255,0.15)", fontSize: "0.65rem", userSelect: "none", flexShrink: 0, textAlign: "right", paddingRight: 14, lineHeight: "1.7" }}>
        {lineNum}
      </span>
      <span className="font-mono" style={{ fontSize: "0.72rem", lineHeight: "1.7" }}>
        <span style={{ color, fontWeight: 700 }}>{seg}</span>
        {elems.map((el, i) => (
          <span key={i}>
            <span style={{ color: "rgba(255,255,255,0.25)" }}>*</span>
            <span style={{ color: el.trim() === "" ? "rgba(255,255,255,0.18)" : "#CBD5E1" }}>{el || "·"}</span>
          </span>
        ))}
        <span style={{ color: "rgba(16,185,129,0.55)", fontWeight: 700 }}>~</span>
      </span>
    </div>
  );
}

// ─── Syntax-highlighted XML line ─────────────────────────────────────────────
function XMLLine({ line, lineNum }: { line: string; lineNum: number }) {
  if (!line.trim()) return (
    <div className="flex">
      <span style={{ width: 38, color: "rgba(255,255,255,0.15)", fontSize: "0.65rem", userSelect: "none", flexShrink: 0, textAlign: "right", paddingRight: 14 }}>{lineNum}</span>
    </div>
  );

  const isComment = line.trim().startsWith("<!--");
  const isDecl    = line.trim().startsWith("<?");
  const isClose   = /^\s*<\//.test(line);

  // Tokenize XML for coloring
  function colorize(raw: string): React.ReactNode[] {
    const tokens: React.ReactNode[] = [];
    let rest = raw;
    let key  = 0;

    while (rest.length) {
      // comment
      const cm = rest.match(/^(<!--.*?-->)([\s\S]*)/);
      if (cm) { tokens.push(<span key={key++} style={{ color: "#475569", fontStyle: "italic" }}>{cm[1]}</span>); rest = cm[2]; continue; }
      // decl
      const dc = rest.match(/^(<\?.*?\?>)([\s\S]*)/);
      if (dc) { tokens.push(<span key={key++} style={{ color: "#64748B" }}>{dc[1]}</span>); rest = dc[2]; continue; }
      // closing tag
      const ct = rest.match(/^(<\/)([\w:.-]+)(>)([\s\S]*)/);
      if (ct) {
        tokens.push(<span key={key++} style={{ color: "#475569" }}>{ct[1]}</span>);
        tokens.push(<span key={key++} style={{ color: "#34D399" }}>{ct[2]}</span>);
        tokens.push(<span key={key++} style={{ color: "#475569" }}>{ct[3]}</span>);
        rest = ct[4]; continue;
      }
      // opening tag
      const ot = rest.match(/^(<)([\w:.-]+)((?:\s[\w:.-]+="[^"]*")*)\s*(\/?>)([\s\S]*)/);
      if (ot) {
        tokens.push(<span key={key++} style={{ color: "#475569" }}>{ot[1]}</span>);
        tokens.push(<span key={key++} style={{ color: "#10B981", fontWeight: 700 }}>{ot[2]}</span>);
        // attrs
        const attrStr = ot[3];
        if (attrStr) {
          const attrParts = attrStr.match(/[\w:.-]+="[^"]*"/g) ?? [];
          attrParts.forEach(a => {
            const [aName, aVal] = a.split("=");
            tokens.push(<span key={key++}> <span style={{ color: "#93C5FD" }}>{aName}</span><span style={{ color: "#475569" }}>=</span><span style={{ color: "#F59E0B" }}>"{aVal?.replace(/"/g, "")}"</span></span>);
          });
        }
        tokens.push(<span key={key++} style={{ color: "#475569" }}>{ot[4]}</span>);
        rest = ot[5]; continue;
      }
      // text / rest
      const tx = rest.match(/^([^<]+)([\s\S]*)/);
      if (tx) { tokens.push(<span key={key++} style={{ color: "#E2E8F0" }}>{tx[1]}</span>); rest = tx[2]; continue; }
      tokens.push(<span key={key++} style={{ color: "#94A3B8" }}>{rest}</span>);
      break;
    }
    return tokens;
  }

  return (
    <div className="flex hover:bg-white/[0.03] rounded transition-colors">
      <span style={{ width: 38, color: "rgba(255,255,255,0.15)", fontSize: "0.65rem", userSelect: "none", flexShrink: 0, textAlign: "right", paddingRight: 14, lineHeight: "1.7" }}>
        {lineNum}
      </span>
      <span className="font-mono" style={{ fontSize: "0.72rem", lineHeight: "1.7" }}>
        {colorize(line)}
      </span>
    </div>
  );
}

// ─── Copy Button ─────────────────────────────────────────────────────────────
function CopyBtn({ text }: { text: string }) {
  const [copied, setCopied] = useState(false);
  const copy = () => {
    navigator.clipboard.writeText(text).catch(() => {});
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };
  return (
    <button
      onClick={copy}
      className="flex items-center gap-1.5 px-3 py-1.5 rounded-lg font-mono transition-all hover:scale-105"
      style={{
        background: copied ? C.emDim : C.glassBg,
        border: `1px solid ${copied ? "rgba(16,185,129,0.38)" : C.glassBorder}`,
        color: copied ? C.em : C.muted, fontSize: "0.62rem", fontWeight: 700,
      }}
    >
      {copied ? <Check style={{ width: 11, height: 11 }} /> : <Copy style={{ width: 11, height: 11 }} />}
      {copied ? "Copied!" : "Copy"}
    </button>
  );
}

