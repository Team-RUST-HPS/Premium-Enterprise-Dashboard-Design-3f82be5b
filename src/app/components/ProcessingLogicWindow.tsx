// ─── Main Export ──────────────────────────────────────────────────────────────
export function ProcessingLogicWindow() {
  const [selectedEdiId, setSelectedEdiId] = useState<string | null>(null);
  const [detailedMode, setDetailedMode]   = useState(false);
  const [targetFormat, setTargetFormat]   = useState<"XML" | "JSON">("XML");
  const [instruction, setInstruction]     = useState("Populate the Member tag in XML according to the 2000 loop in EDI 834.");
  const [rules, setRules]                 = useState<LogicRule[]>(INITIAL_RULES);
  const [processing, setProcessing]       = useState(false);
  const [showPreview, setShowPreview]     = useState(false);

  const highlightedTargetIds = useMemo((): Set<string> => {
    if (!selectedEdiId) return new Set();
    function findNode(nodes: EDINode[], id: string): EDINode | null {
      for (const n of nodes) { if (n.id === id) return n; if (n.children) { const f = findNode(n.children, id); if (f) return f; } }
      return null;
    }
    const node = findNode(EDI_TREE_DATA, selectedEdiId);
    if (!node) return new Set();
    return new Set(getTargetMappings(node));
  }, [selectedEdiId]);

  const highlightedEdiIds = useMemo((): Set<string> => new Set(rules.filter(r => r.sourceLoopId).map(r => r.sourceLoopId!)), [rules]);

  const activeRule = useMemo(() => {
    if (!selectedEdiId) return null;
    return rules.find(r => r.sourceLoopId === selectedEdiId) ?? null;
  }, [selectedEdiId, rules]);

  const handleGenerate = useCallback(() => {
    setProcessing(true);
    setTimeout(() => {
      const newRule: LogicRule = {
        id: `r${Date.now()}`, source: "EDI Loop 2000", target: `${targetFormat} Member`,
        operation: "Create node for each loop instance", confirmed: false,
        sourceLoopId: "loop-2000", targetNodeId: "member",
      };
      setRules(prev => {
        const exists = prev.some(r => r.source === newRule.source && r.target === newRule.target);
        return exists ? prev : [newRule, ...prev];
      });
      setProcessing(false);
    }, 1200);
  }, [instruction, targetFormat]);

  const confirmRule = (id: string) => setRules(prev => prev.map(r => r.id === id ? { ...r, confirmed: true } : r));
  const removeRule  = (id: string) => setRules(prev => prev.filter(r => r.id !== id));
  const targetTree  = targetFormat === "XML" ? XML_TARGET : JSON_TARGET;

  return (
    <>
      {/* ── Translation Preview Overlay (portal-like fixed layer) ── */}
      {showPreview && (
        <TranslationPreviewOverlay
          targetFormat={targetFormat}
          onClose={() => setShowPreview(false)}
        />
      )}

      <div style={{ display: "flex", flexDirection: "column", height: "100%", overflow: "hidden" }}>
        {/* ── Top Bar ── */}
        <div
          className="flex items-center gap-4 px-6 py-3 flex-shrink-0"
          style={{ borderBottom: `1px solid ${C.glassBorder}`, background: C.glassBg }}
        >
          <div className="flex items-center gap-2">
            <Layers style={{ width: 15, height: 15, color: C.em }} />
            <span className="font-mono" style={{ color: C.text, fontSize: "0.8rem", fontWeight: 700 }}>
              Processing Logic Configuration
            </span>
            <span className="font-mono px-2 py-0.5 rounded" style={{ background: C.emDim, border: "1px solid rgba(16,185,129,0.30)", color: C.em, fontSize: "0.58rem", fontWeight: 700, letterSpacing: "0.08em" }}>
              {rules.filter(r => r.confirmed).length}/{rules.length} CONFIRMED
            </span>
          </div>

          <div className="ml-auto flex items-center gap-3">
            <StructureToggle detailed={detailedMode} onChange={setDetailedMode} />

            {/* Format toggle */}
            <div className="flex items-center gap-1 rounded-lg p-0.5" style={{ background: C.glassBg, border: `1px solid ${C.glassBorder}` }}>
              {(["XML", "JSON"] as const).map(fmt => (
                <button key={fmt} onClick={() => setTargetFormat(fmt)} className="font-mono rounded-md px-3 py-1.5 transition-all"
                  style={{ background: targetFormat === fmt ? C.purpleDim : "transparent", border: targetFormat === fmt ? "1px solid rgba(139,92,246,0.38)" : "1px solid transparent", color: targetFormat === fmt ? C.purple : C.muted, fontSize: "0.62rem", fontWeight: 700 }}>
                  {fmt}
                </button>
              ))}
            </div>

            {/* Preview Translation Button */}
            <button
              onClick={() => setShowPreview(true)}
              className="flex items-center gap-2 px-4 py-2 rounded-xl font-mono transition-all hover:scale-105"
              style={{
                background: "rgba(16,185,129,0.12)",
                border: "1px solid rgba(16,185,129,0.40)",
                color: C.em, fontSize: "0.7rem", fontWeight: 700,
                boxShadow: `0 0 18px rgba(16,185,129,0.15)`,
                textShadow: C.emTextGlow,
              }}
            >
              <Eye style={{ width: 13, height: 13 }} />
              Preview Translation
            </button>
          </div>
        </div>

        {/* ── Main Three-Panel Workspace ── */}
        <div style={{ flex: 1, minHeight: 0, overflow: "hidden" }}>
          <PanelGroup direction="horizontal" style={{ height: "100%" }}>

            {/* LEFT: EDI Source Structure */}
            <Panel defaultSize={26} minSize={18}>
              <div style={{ height: "100%", display: "flex", flexDirection: "column", borderRight: `1px solid ${C.glassBorder}` }}>
                <div className="flex items-center gap-2 px-4 py-3 flex-shrink-0" style={{ borderBottom: `1px solid ${C.glassBorder}`, background: "rgba(59,130,246,0.05)" }}>
                  <FileCode2 style={{ width: 13, height: 13, color: C.info }} />
                  <span className="font-mono" style={{ color: C.info, fontSize: "0.68rem", fontWeight: 700, letterSpacing: "0.08em" }}>EDI SOURCE</span>
                  <span className="ml-auto font-mono px-2 py-0.5 rounded" style={{ background: "rgba(59,130,246,0.1)", border: "1px solid rgba(59,130,246,0.25)", color: C.info, fontSize: "0.55rem", fontWeight: 700 }}>834 / X12</span>
                </div>

                {selectedEdiId && (
                  <div className="mx-3 mt-2.5 px-3 py-2 rounded-lg flex items-center gap-2 flex-shrink-0" style={{ background: C.infoDim, border: "1px solid rgba(59,130,246,0.25)" }}>
                    <ArrowRight style={{ width: 10, height: 10, color: C.info, flexShrink: 0 }} />
                    <span style={{ color: C.info, fontSize: "0.62rem" }}>Highlighted target nodes synced</span>
                  </div>
                )}

                <div className="overflow-y-auto flex-1 p-3" style={{ scrollbarWidth: "thin" }}>
                  {EDI_TREE_DATA.map(node => (
                    <EDITreeNode key={node.id} node={node} depth={0} selectedId={selectedEdiId} onSelect={setSelectedEdiId} highlightIds={highlightedEdiIds} detailedMode={detailedMode} />
                  ))}
                </div>

                {selectedEdiId && (
                  <button onClick={() => setSelectedEdiId(null)} className="mx-3 mb-3 py-1.5 rounded-lg font-mono flex-shrink-0" style={{ background: C.glassBg, border: `1px solid ${C.glassBorder}`, color: C.muted, fontSize: "0.62rem" }}>
                    Clear Selection
                  </button>
                )}
              </div>
            </Panel>

            <PanelResizeHandle style={{ width: 4, background: C.glassBorder, cursor: "col-resize" }} />

            {/* CENTER: Processing Logic Editor */}
            <Panel defaultSize={40} minSize={28}>
              <div style={{ height: "100%", display: "flex", flexDirection: "column" }}>
                <div className="flex items-center gap-2 px-4 py-3 flex-shrink-0" style={{ borderBottom: `1px solid ${C.glassBorder}`, background: "rgba(16,185,129,0.04)" }}>
                  <Sparkles style={{ width: 13, height: 13, color: C.em }} />
                  <span className="font-mono" style={{ color: C.em, fontSize: "0.68rem", fontWeight: 700, letterSpacing: "0.08em" }}>PROCESSING LOGIC</span>
                </div>

                <div className="flex-1 overflow-y-auto p-4 space-y-4" style={{ scrollbarWidth: "thin" }}>
                  {/* Natural language editor */}
                  <div className="rounded-xl overflow-hidden" style={{ border: "1px solid rgba(16,185,129,0.28)" }}>
                    <div className="flex items-center gap-2 px-4 py-2.5" style={{ background: "rgba(16,185,129,0.08)", borderBottom: "1px solid rgba(16,185,129,0.18)" }}>
                      <Edit3 style={{ width: 11, height: 11, color: C.em }} />
                      <span className="font-mono" style={{ color: C.em, fontSize: "0.62rem", fontWeight: 700, letterSpacing: "0.08em" }}>LOGIC INSTRUCTION</span>
                    </div>
                    <textarea
                      value={instruction}
                      onChange={e => setInstruction(e.target.value)}
                      placeholder="Describe the transformation in plain language…"
                      className="w-full font-mono outline-none resize-none"
                      style={{ background: "rgba(4,9,20,0.55)", color: C.text, fontSize: "0.82rem", lineHeight: 1.7, padding: "14px 16px", minHeight: 90, border: "none" }}
                    />
                    <div className="flex items-center gap-3 px-4 py-2.5" style={{ background: "rgba(4,9,20,0.35)", borderTop: "1px solid rgba(16,185,129,0.12)" }}>
                      <span style={{ color: C.muted, fontSize: "0.65rem" }}>{instruction.length} chars</span>
                      <button
                        onClick={handleGenerate}
                        disabled={processing || !instruction.trim()}
                        className="ml-auto flex items-center gap-2 px-4 py-2 rounded-lg font-mono transition-all hover:scale-105"
                        style={{
                          background: processing ? "rgba(16,185,129,0.08)" : C.em,
                          border: `1px solid ${processing ? "rgba(16,185,129,0.30)" : "transparent"}`,
                          color: processing ? C.em : "#fff",
                          fontSize: "0.7rem", fontWeight: 700,
                          boxShadow: !processing ? `0 0 16px ${C.emGlow}` : "none",
                          opacity: !instruction.trim() ? 0.5 : 1,
                        }}
                      >
                        {processing
                          ? <><div className="w-3 h-3 rounded-full border-2 border-t-transparent animate-spin" style={{ borderColor: `rgba(16,185,129,0.25)`, borderTopColor: C.em }} />Generating…</>
                          : <><Play style={{ width: 11, height: 11 }} />Generate Rules</>
                        }
                      </button>
                    </div>
                  </div>

                  {activeRule && <FlowIndicator rule={activeRule} />}

                  <div>
                    <div className="flex items-center justify-between mb-2.5">
                      <span className="font-mono" style={{ color: C.muted, fontSize: "0.65rem", fontWeight: 700, letterSpacing: "0.08em" }}>INTERPRETED RULES · {rules.length}</span>
                      <span className="font-mono px-2 py-0.5 rounded" style={{ background: "rgba(16,185,129,0.08)", border: "1px solid rgba(16,185,129,0.22)", color: C.em, fontSize: "0.58rem", fontWeight: 700 }}>
                        {rules.filter(r => r.confirmed).length} CONFIRMED
                      </span>
                    </div>
                    <div className="space-y-2.5">
                      {rules.map(rule => (
                        <LogicRuleCard
                          key={rule.id} rule={rule}
                          onConfirm={confirmRule} onRemove={removeRule}
                          isHighlighted={!!selectedEdiId && (rule.sourceLoopId === selectedEdiId || flattenIds(EDI_TREE_DATA.find(n => n.id === selectedEdiId) ?? { id: "", name: "", type: "segment" }).includes(rule.sourceLoopId ?? ""))}
                        />
                      ))}
                      {rules.length === 0 && (
                        <div className="py-8 text-center rounded-xl" style={{ border: `1px dashed ${C.glassBorder}`, color: C.muted, fontSize: "0.75rem" }}>
                          No rules yet — write an instruction and click Generate
                        </div>
                      )}
                    </div>
                  </div>
                </div>
              </div>
            </Panel>

            <PanelResizeHandle style={{ width: 4, background: C.glassBorder, cursor: "col-resize" }} />

            {/* RIGHT: Target Structure + Config Preview */}
            <Panel defaultSize={34} minSize={22}>
              <div style={{ height: "100%", display: "flex", flexDirection: "column", borderLeft: `1px solid ${C.glassBorder}` }}>
                <div className="flex items-center gap-2 px-4 py-3 flex-shrink-0" style={{ borderBottom: `1px solid ${C.glassBorder}`, background: "rgba(139,92,246,0.05)" }}>
                  <Code2 style={{ width: 13, height: 13, color: C.purple }} />
                  <span className="font-mono" style={{ color: C.purple, fontSize: "0.68rem", fontWeight: 700, letterSpacing: "0.08em" }}>TARGET · {targetFormat}</span>
                  {highlightedTargetIds.size > 0 && (
                    <span className="ml-auto font-mono px-2 py-0.5 rounded animate-pulse" style={{ background: C.emDim, border: "1px solid rgba(16,185,129,0.35)", color: C.em, fontSize: "0.55rem", fontWeight: 700 }}>
                      {highlightedTargetIds.size} SYNCED
                    </span>
                  )}
                </div>

                {highlightedTargetIds.size > 0 && (
                  <div className="mx-3 mt-2.5 px-3 py-2 rounded-lg flex items-center gap-2 flex-shrink-0" style={{ background: C.emDim, border: "1px solid rgba(16,185,129,0.28)" }}>
                    <Zap style={{ width: 10, height: 10, color: C.em, flexShrink: 0 }} />
                    <span style={{ color: C.em, fontSize: "0.62rem" }}>Synced from EDI selection</span>
                  </div>
                )}

                <div className="flex-1 overflow-y-auto p-3" style={{ scrollbarWidth: "thin" }}>
                  <TargetTreeNode node={targetTree} depth={0} highlightIds={highlightedTargetIds} detailedMode={detailedMode} />
                </div>

                <div className="p-3 flex-shrink-0" style={{ borderTop: `1px solid ${C.glassBorder}` }}>
                  <BackendJSONPreview rules={rules} targetFormat={targetFormat} />
                </div>
              </div>
            </Panel>

          </PanelGroup>
        </div>
      </div>
    </>
  );
}
